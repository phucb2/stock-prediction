"""Build Submit.ipynb from Sample Assignment.ipynb + authors.md + explains.md + output/py_bundle.txt."""

from __future__ import annotations

import argparse
import copy
import importlib.util
import json
import os
import re
import uuid
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
TEMPLATE_NOTEBOOK = ROOT / "Sample Assignment.ipynb"
OUTPUT_NOTEBOOK = ROOT / "Submit.ipynb"
AUTHORS_MD = ROOT / "authors.md"
EXPLAINS_MD = ROOT / "explains.md"
BUNDLE_PY_SCRIPT = ROOT / "scripts" / "bundle_py.py"
OUTPUT_DIR = ROOT / "output"
PY_BUNDLE_TXT = OUTPUT_DIR / "py_bundle.txt"
PY_BUNDLE_MANIFEST = OUTPUT_DIR / "py_bundle_manifest.txt"

# Default α for ``_ses = SimpleExponentialSmoothing(alpha=...)`` in the notebook cell.
DEFAULT_SES_ALPHA = 0.3

_MD_CELL_SPLIT = re.compile(r"^\s*---\s*$", re.MULTILINE)


def _load_bundle_py_module():
    """Load ``scripts/bundle_py.py`` (same rules as ``make bundle``)."""
    if not BUNDLE_PY_SCRIPT.is_file():
        raise SystemExit(f"scripts/bundle_py.py not found: {BUNDLE_PY_SCRIPT}")
    spec = importlib.util.spec_from_file_location("bundle_py", BUNDLE_PY_SCRIPT)
    if spec is None or spec.loader is None:
        raise SystemExit(f"Could not load {BUNDLE_PY_SCRIPT}")
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


def _iter_bundle_py_files(root: Path) -> list[Path]:
    """Same paths as ``make bundle`` / ``scripts/bundle_py.iter_py_files``."""
    return _load_bundle_py_module().iter_py_files(root)


def _ensure_py_bundle_txt(bundle_mod, root: Path, paths: list[Path]) -> str:
    """
    Write ``output/py_bundle_manifest.txt`` and ``output/py_bundle.txt`` like ``make bundle``,
    then return the bundle file text for the notebook cell.
    """
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    bundle_mod.write_manifest(paths, root, PY_BUNDLE_MANIFEST)
    bundle_mod.write_bundle(paths, root, PY_BUNDLE_TXT)
    return PY_BUNDLE_TXT.read_text(encoding="utf-8")


def build_prediction_cell_from_py_bundle(bundle_text: str) -> str:
    """
    Build the customize cell: comment header + ``py_bundle.txt`` body + ``prediction()`` wrapper.
    """
    header = (
        "# customize your prediction - inlined from output/py_bundle.txt "
        "(regenerated by submit via scripts/bundle_py.py)\n\n"
    )
    footer = (
        f"\n_ses = SimpleExponentialSmoothing(alpha={DEFAULT_SES_ALPHA!r})\n\n\n"
        "def prediction(P, V, h=20):\n"
        "    return _ses.predict(P, V, h)\n"
    )
    if not bundle_text.endswith("\n"):
        bundle_text += "\n"
    return header + bundle_text + footer


def _resolve_ses_path_for_submit(bundle_files: list[Path]) -> Path:
    """``SES.py`` at project root must be part of the bundle (``predict`` injection source)."""
    expected = (ROOT / "SES.py").resolve()
    for p in bundle_files:
        if p.resolve() == expected:
            return p
    raise SystemExit(
        "SES.py must exist at the project root and be included in the bundle "
        "(same rules as scripts/bundle_py.py; excludes main.py, tests/, scripts/). "
        f"Expected: {ROOT / 'SES.py'}"
    )
_HTML_COMMENT = re.compile(r"<!--.*?-->", re.DOTALL)


def _strip_md_comments(text: str) -> str:
    return _HTML_COMMENT.sub("", text).strip()


def _md_segments(path: Path) -> list[str]:
    raw = path.read_text(encoding="utf-8")
    parts = [p.strip() for p in _MD_CELL_SPLIT.split(raw) if p.strip()]
    if not parts:
        return [_strip_md_comments(raw)]
    return [_strip_md_comments(p) for p in parts]


def first_cell_from_authors_md(path: Path) -> str:
    """First ``---``-delimited segment in authors.md (or whole file)."""
    segs = _md_segments(path)
    return segs[0]


def second_cell_from_explains_md(path: Path) -> str:
    """Second ``---``-delimited segment in explains.md; if only one, use whole file."""
    segs = _md_segments(path)
    if len(segs) >= 2:
        return segs[1]
    return segs[0]


def _first_non_empty_line(text: str) -> str | None:
    for line in text.splitlines():
        if line.strip():
            return line
    return None


def _cell_starts_with_customize_prediction(src: str) -> bool:
    first = _first_non_empty_line(src)
    return bool(first and first.strip().startswith("# customize your prediction"))


def _is_duplicate_ma_placeholder_cell(src: str) -> bool:
    """Old MA-only ``# customize your prediction`` block (must drop after SES cell)."""
    first = _first_non_empty_line(src)
    if not first or first.strip() != "# customize your prediction":
        return False
    return "SimpleExponentialSmoothing" not in src and "def prediction(" in src


def _find_template_ses_prediction_cell_index(cells: list) -> int:
    """Index of the code cell whose first line starts with ``# customize your prediction`` and defines SES."""
    for i, cell in enumerate(cells):
        if cell.get("cell_type") != "code":
            continue
        src = _cell_source_str(cell)
        if _cell_starts_with_customize_prediction(src) and "class SimpleExponentialSmoothing" in src:
            return i
    raise SystemExit(
        "Template must have a SES prediction cell: first non-empty line starts with "
        "'# customize your prediction' and defines class SimpleExponentialSmoothing."
    )


def _to_nb_source(text: str) -> list[str]:
    if not text.endswith("\n"):
        text += "\n"
    return [line if line.endswith("\n") else line + "\n" for line in text.splitlines(True)]


def _cell_source_str(cell) -> str:
    src = cell.get("source", "")
    if isinstance(src, list):
        return "".join(src)
    return src


def _trim_notebook_to_assignment_execute(nb) -> None:
    """
    Drop optional cells after the course ``# execute`` / ``evaluate(p, t, ...)`` cell.

    The template may include extra demos (e.g. other tickers) that need files not in sample_data/.
    """
    for i, cell in enumerate(nb.cells):
        if cell.get("cell_type") != "code":
            continue
        src = _cell_source_str(cell)
        if "# execute" in src and "evaluate(p, t" in src:
            nb.cells = nb.cells[: i + 1]
            return


def verify_submit_notebook_runs(nb_path: Path, cwd: Path) -> None:
    """Execute assignment cells with nbclient; cwd must be project root (for ./sample_data)."""
    os.environ.setdefault("MPLBACKEND", "Agg")

    import nbformat
    from nbclient import NotebookClient
    from nbclient.exceptions import CellExecutionError

    nb = nbformat.read(nb_path, as_version=4)
    _trim_notebook_to_assignment_execute(nb)
    resources = {"metadata": {"path": str(cwd)}}
    client = NotebookClient(nb, timeout=300, kernel_name="python3", resources=resources)
    try:
        client.execute()
    except CellExecutionError as e:
        raise SystemExit(
            f"Execution check failed (fix cells or data under sample_data/):\n{e!s}\n"
        ) from e


def build_submit_notebook() -> None:
    if not TEMPLATE_NOTEBOOK.is_file():
        raise SystemExit(f"Template notebook not found: {TEMPLATE_NOTEBOOK}")
    if not AUTHORS_MD.is_file():
        raise SystemExit(f"authors.md not found: {AUTHORS_MD}")
    if not EXPLAINS_MD.is_file():
        raise SystemExit(f"explains.md not found: {EXPLAINS_MD}")

    bundle_mod = _load_bundle_py_module()
    bundle_files = bundle_mod.iter_py_files(ROOT)
    if not bundle_files:
        raise SystemExit(
            "No project .py sources matched the bundle rules (see scripts/bundle_py.py)."
        )
    _resolve_ses_path_for_submit(bundle_files)
    bundle_text = _ensure_py_bundle_txt(bundle_mod, ROOT, bundle_files)

    cell_authors = first_cell_from_authors_md(AUTHORS_MD)
    cell_explains = second_cell_from_explains_md(EXPLAINS_MD)

    with TEMPLATE_NOTEBOOK.open(encoding="utf-8") as f:
        nb = json.load(f)

    cells = nb.get("cells", [])
    if len(cells) < 5:
        raise SystemExit(f"Expected at least 5 cells, found {len(cells)}")

    pred_idx = _find_template_ses_prediction_cell_index(cells)
    if pred_idx not in (3, 4):
        raise SystemExit(
            "SES prediction cell must be at index 3 or 4 in Sample Assignment.ipynb."
        )
    data_idx = 7 - pred_idx

    pred_cell = copy.deepcopy(cells[pred_idx])
    pred_cell["cell_type"] = "code"
    pred_cell["source"] = _to_nb_source(
        build_prediction_cell_from_py_bundle(bundle_text)
    )
    pred_cell["outputs"] = []
    pred_cell["execution_count"] = None

    template_data_cell = copy.deepcopy(cells[data_idx])
    template_data_cell["id"] = uuid.uuid4().hex
    template_data_cell["outputs"] = []
    template_data_cell["execution_count"] = None

    cells[0]["cell_type"] = "markdown"
    cells[0]["source"] = _to_nb_source(cell_authors)
    cells[0]["outputs"] = []
    cells[0]["execution_count"] = None

    cells[1]["cell_type"] = "markdown"
    cells[1]["source"] = _to_nb_source(cell_explains)
    cells[1]["outputs"] = []
    cells[1]["execution_count"] = None

    # Template: data load and SES cell at indices 3 and 4; move prediction to index 3.
    cells[3] = pred_cell
    cells[4] = template_data_cell

    if len(cells) > 5 and _is_duplicate_ma_placeholder_cell(_cell_source_str(cells[5])):
        del cells[5]

    with OUTPUT_NOTEBOOK.open("w", encoding="utf-8", newline="\n") as f:
        json.dump(nb, f, ensure_ascii=False, indent=1)
        f.write("\n")

    print(f"Wrote: {OUTPUT_NOTEBOOK}")
    print(f"  (template: {TEMPLATE_NOTEBOOK.relative_to(ROOT)}, unchanged)")
    print(f"  - cell 1 from: {AUTHORS_MD.relative_to(ROOT)} (first segment)")
    print(f"  - cell 2 from: {EXPLAINS_MD.relative_to(ROOT)} (second segment, or whole file)")
    print(
        f"  - prediction cell (now index 3): {PY_BUNDLE_TXT.relative_to(ROOT)} "
        f"({len(bundle_files)} module(s) via scripts/bundle_py.py) + ``prediction()`` wrapper; "
        f"data load at index 4"
    )


def main() -> None:
    ap = argparse.ArgumentParser(description="Build Submit.ipynb and optionally verify execution.")
    ap.add_argument(
        "--skip-verify",
        action="store_true",
        help="Do not execute Submit.ipynb after writing (faster; skips run check).",
    )
    ap.add_argument(
        "--verify-only",
        action="store_true",
        help="Only run execution check on existing Submit.ipynb (no rebuild).",
    )
    args = ap.parse_args()

    if args.verify_only:
        if not OUTPUT_NOTEBOOK.is_file():
            raise SystemExit(f"Missing {OUTPUT_NOTEBOOK}; run without --verify-only first.")
        print(f"Verifying: {OUTPUT_NOTEBOOK.relative_to(ROOT)}")
        verify_submit_notebook_runs(OUTPUT_NOTEBOOK, ROOT)
        print("OK: Submit.ipynb runs successfully (assignment cells executed through # execute).")
        return

    build_submit_notebook()

    if args.skip_verify:
        print("(skipped execution check; use without --skip-verify or: python scripts/submit_notebook.py --verify-only)")
        return

    print()
    print(f"Verifying execution: {OUTPUT_NOTEBOOK.relative_to(ROOT)}")
    verify_submit_notebook_runs(OUTPUT_NOTEBOOK, ROOT)
    print("OK: Submit.ipynb runs successfully (assignment cells executed through # execute).")


if __name__ == "__main__":
    main()
